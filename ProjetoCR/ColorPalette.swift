import Foundation

import UIKit

extension UIColor {
    
    static func azul() -> UIColor {
    return UIColor(red: 136/255, green: 208/255, blue: 243/255, alpha: 1.0)
    }
    
    static func laranja() -> UIColor {
        return UIColor(red: 227/255, green: 148/255, blue: 80/255, alpha: 1.0)
    }
    
    static func verde() -> UIColor {
        return UIColor(red: 31/255, green: 195/255, blue: 45/255, alpha: 1.0)
    }

}